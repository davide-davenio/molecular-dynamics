function [pot] = polyPotential(x, rpoly, rc, eps, sig)
    % computes poly junction to 7th order

    Apoly=(1/((rc - rpoly)^7*rpoly^12))*4*eps*rc^4*sig^6* ...
      (2* rpoly^6 *(-42* rc^3 + 182* ...
       rc^2* rpoly - 273* rc* rpoly^2 + 143* rpoly^3) + (455* rc^3 - 1729* ...
       rc^2* rpoly + 2223* rc* rpoly^2 - 969* rpoly^3)* sig^6);

    Bpoly=(1/((rc - rpoly)^7* rpoly^13))*16* eps* rc^3* sig^6* ...
      (rpoly^6* (54* rc^4 - 154* rc^3* rpoly + ...
       351* rc *rpoly^3 - 286* rpoly^4) + (-315* rc^4 + 749 *rc^3 * rpoly + ... 
       171 *rc^2* rpoly^2 - 1539* rc* rpoly^3 + 969* rpoly^4)* sig^6);
      
    Cpoly=(1/((rc - rpoly)^7* rpoly^14))*12* eps* rc^2* sig^6* ...
      (rpoly^6* (-63* rc^5 - 7* rc^4 *rpoly + 665* rc^3 *rpoly^2 - ...
       975* rc^2* rpoly^3 - 52* rc* rpoly^4 + 572* rpoly^5) + ... 
       2 *(195* rc^5 + 91* rc^4* rpoly - 1781* rc^3* rpoly^2 + ...
       1995 *rc^2* rpoly^3 + 399* rc* rpoly^4 - 969* rpoly^5)* sig^6);
   
    Dpoly=(1/((rc - rpoly)^7* rpoly^15))*16* eps* sig^6* ...
      (rc* rpoly^6* (14* rc^6 + 126* rc^5* rpoly - 420* rc^4* rpoly^2 ...
       - 90* rc^3* rpoly^3 + 1105* rc^2* rpoly^4 - 624* rc* rpoly^5 - ...
       286 *rpoly^6) + rc* (-91* rc^6 - 819* rc^5* rpoly + 2145 * ...
       rc^4 * rpoly^2 + 1125* rc^3* rpoly^3 - 5035* rc^2* rpoly^4 + ...
       1881* rc* rpoly^5 + 969* rpoly^6)* sig^6);
      
    Epoly=(1/((rc - rpoly)^7* rpoly^15))*4* eps* sig^6* ...
      (2* rpoly^6* (-112* rc^6 - 63* rc^5* rpoly + 1305* rc^4* rpoly^2 ...
       - 1625* rc^3* rpoly^3 - 585* rc^2* rpoly^4 + ... 
       1287 *rc* rpoly^5 + 143* rpoly^6) + (1456*rc^6 +1404*rc^5* rpoly - ... 
       14580 *rc^4* rpoly^2 + 13015* rc^3* rpoly^3 + 7695* rc^2* rpoly^4 - ... 
       8721 *rc* rpoly^5 - 969* rpoly^6)* sig^6);
      
    Fpoly=(1/((rc - rpoly)^7* rpoly^15))*48* eps* sig^6* ...
      (-rpoly^6* (-28* rc^5 + 63* rc^4* rpoly + 65* rc^3* rpoly^2 - ...
       247* rc^2* rpoly^3 + 117* rc* rpoly^4 + 65* rpoly^5) + ...
       (-182* rc^5 + 312* rc^4* rpoly + 475* rc^3* rpoly^2 - ...
       1140* rc^2* rpoly^3 + 342* rc* rpoly^4 + 228* rpoly^5)* sig^6);
      
    Gpoly=(1/((rc - rpoly)^7* rpoly^15))*4* eps* sig^6* ...
      (rpoly^6* (-224* rc^4 + 819* rc^3* rpoly - 741* rc^2* rpoly^2 - ...
       429* rc* rpoly^3 + 715* rpoly^4) + 2 *(728* rc^4 - ...
       2223* rc^3* rpoly + 1425* rc^2* rpoly^2 ... 
       + 1292* rc* rpoly^3 - 1292* rpoly^4)* sig^6);
  
    Hpoly=(1/((rc - rpoly)^7* rpoly^15))*16* eps* sig^6* ...
      (rpoly^6 * (14* rc^3 - 63* rc^2* rpoly + 99* rc* rpoly^2 - 55* rpoly^3) + ...
      (-91* rc^3 + 351* rc^2* rpoly - 459* rc* rpoly^2 + 204* rpoly^3)* sig^6);

    pot = Apoly+Bpoly*x+Cpoly*x^2+Dpoly*x^3+Epoly*x^4+Fpoly*x^5+Gpoly*x^6+Hpoly*x^7;
end

